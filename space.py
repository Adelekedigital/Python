
name = input("what's your name? ")

age = int(input("how old are you? "))


# concatenation

print(name + " you are doing great and you are " + str(age) + " years old")


# comma spacing

print(name,"you are doing great and you are",str(age),"years old")


# format(f) spacing

print(f"{name} you are doing great and you are {str(age)} years old")